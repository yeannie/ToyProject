package java_100_coding_0303;

public class java003_for_Iteration {
						//Iteration : 반복
	public static void main(String [] args) {
/*****************************		
 		자바의 반복문 
		1)for
		2)while
		3)do~while
		4)향상된 for
****************************/	
		// 1_ for
		// for (변수초기식; 조건식; 증감식){ 조건식 --> 참인 동안 실행 }
		for(int i = 0; i<10; i++ ) {
			System.out.print(i);
			System.out.println(0);
			System.out.println(i);
			System.out.print(i+"");
		}
		System.out.println();
	}
}
